package Operadores;
public class IncrementacaoDecrementacao {
    public static void main(String[] args) {
        
         // x repetição
         int number = 5;

         // numero = numero +1;
 
         System.out.println(-- number);
         System.out.println(number);
 
         boolean variavel = true;
 
         System.out.println(!variavel);
 
         variavel = !variavel;
 
         System.out.println(variavel);
         
    }
}
